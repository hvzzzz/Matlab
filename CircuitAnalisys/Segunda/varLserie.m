% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)

a = str2double(get(handles.xl,'string'));
b = str2double(get(handles.w,'string'));
c = str2double(get(handles.R,'string'));
d = str2double(get(handles.Vo,'string'));

g=0:0.05:4*a/b;
xc=g*b;
I=d./sqrt(c^2+(a-xc).^2);
Vc=d*xc./sqrt(c^2+(a-xc).^2);
Vl=d*a./sqrt(c^2+(a-xc).^2);

plot(g,Vc,'r',g,Vl,'b',g,I,'g',g,xc,'c','linewidth',1.5);

zoom on
grid on
legend('Vc','Vl','I','xl')
J=d./c;
A2=J*a;
xlm=(a^2+c^2)/a;
ivlm=d./sqrt(c^2+(xlm-a)^2);
vlm=ivlm*xlm;

set(handles.vcr,'String',A2)
set(handles.vlr,'String',A2)
set(handles.imax,'String',J)
set(handles.xlmax,'String',xlm)
set(handles.vlmax,'String',vlm)









